package com.trailblazer.demo.models;

/**
 * The Class SeatInfo.
 */
public class SeatInfo {
    
    /** The id. */
    private String id;
    
    /** The lat long. */
    private String latLong;

	/**
	 * Gets the id.
	 *
	 * @return the id
	 */
	public String getId() {
		return id;
	}

	/**
	 * Sets the id.
	 *
	 * @param id the new id
	 */
	public void setId(String id) {
		this.id = id;
	}

	/**
	 * Gets the lat long.
	 *
	 * @return the lat long
	 */
	public String getLatLong() {
		return latLong;
	}

	/**
	 * Sets the lat long.
	 *
	 * @param latLong the new lat long
	 */
	public void setLatLong(String latLong) {
		this.latLong = latLong;
	}
}
